#Upper case 
    #using upper() method
a = "Thiago"
print(a.upper())
print("------------")

#Lower Case 
    #using lower() method 
a = "Thiago"
print(a.lower())
print("------------") 

#Remove Whitespace 
    #Whitespace is the space before and/or after the actual text
    #using strip() method 
a = " Thiago Zilli "
print(a.strip()) 
print("------------")

#Replace String
    #using replace() method 
a = "Thiago Zilli"
print(a.replace("Thiago Zilli", "ziiz"))
print("------------")

#Split String 
    #using split() method
    #return a list
    #Specified separator 
a = "Thiago, Zilli"
print(a.split(",")) #returns ['Thiago', 'Zilli'] 
print("------------")


