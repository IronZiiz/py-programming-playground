# A dictionary is a collection which is ordered*, changeable and do not allow duplicates.
myDict = {
    "name": "Thiago",
    "last name": "Zilli",
    "year": 17
}
print(myDict)
print("------------")

# Dictionary Items
myDict = {
    "name": "Thiago",
    "last name": "Zilli",
    "year": 17
}
print(myDict["name"])
print("------------")

# Dictionaries are ordered 
# Dictionaries are changeable
# Dictionaries cannot have two items with the same key

# Dictionary lenght 
myDict = {
    "name": "Thiago",
    "last name": "Zilli",
    "year": 17
}
print(len(myDict))
print("------------")

