#Return a range of character by using the slice syntax 

a = "Thiago Zilli"
print(a[2:6])
print("------------") 

#slice from the start 
a = "Thiago Zilli"
print(a[:6]) #Omit first index 
print("------------") 

#slice to the end 
a = "Thiago Zilli"
print(a[2:]) #Omit the final index
print("------------") 

#Negative Indexing 
a = "Thiago Zilli"
print(a[-5: -2]) #Start the slice from the end of the string 
print("------------") 


